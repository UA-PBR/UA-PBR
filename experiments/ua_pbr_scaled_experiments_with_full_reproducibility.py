def estimate_lipschitz(model, dataloader, device, n_samples=100):
    model.eval()
    L_estimates = []
    data_iter = iter(dataloader)

    for _ in range(n_samples):
        try:
            x, _, _ = next(data_iter)
        except StopIteration:
            data_iter = iter(dataloader)
            x, _, _ = next(data_iter)

        x = x.to(device)
        x1 = x + 0.01 * torch.randn_like(x)
        x2 = x + 0.01 * torch.randn_like(x)

        with torch.no_grad():
            out1 = model(x1)
            out2 = model(x2)

        dist_out = torch.norm(out1 - out2, dim=1).mean().item()
        dist_in = torch.norm((x1 - x2).view(x.size(0), -1), dim=1).mean().item()

        if dist_in > 1e-8:
            L_estimates.append(dist_out / dist_in)

    return np.mean(L_estimates) if L_estimates else 1.0

class ThresholdOptimizer:
    def __init__(self, lambda_cost=0.3):
        self.lambda_cost = lambda_cost

    def compute_risk(self, phy_scores, unc_scores, labels, preds, tau_phy, tau_unc):
        reject_phy = phy_scores > tau_phy
        reject_unc = (~reject_phy) & (unc_scores > tau_unc)
        accept = (~reject_phy) & (~reject_unc)

        risk = (reject_phy.sum() + reject_unc.sum()) * self.lambda_cost
        risk += ((~reject_phy) & (~reject_unc) & (preds != labels)).sum()
        return risk / len(labels)

    def optimize(self, phy_scores, unc_scores, labels, preds, grid_size=40):
        best_risk = float('inf')
        best_tau_phy = None
        best_tau_unc = None
        best_p_phy, best_p_unc = None, None

        for p_phy in np.linspace(10, 90, grid_size):
            for p_unc in np.linspace(10, 90, grid_size):
                tau_phy = np.percentile(phy_scores, p_phy)
                tau_unc = np.percentile(unc_scores, p_unc)
                risk = self.compute_risk(phy_scores, unc_scores, labels, preds, tau_phy, tau_unc)

                if risk < best_risk:
                    best_risk = risk
                    best_tau_phy = tau_phy
                    best_tau_unc = tau_unc
                    best_p_phy = p_phy
                    best_p_unc = p_unc

        return best_tau_phy, best_tau_unc, best_risk, best_p_phy, best_p_unc

class EvaluationMetrics:
    @staticmethod
    def rejection_quality(phy_scores, unc_scores, labels, preds, tau_phy, tau_unc, lambda_cost):
        reject_phy = phy_scores > tau_phy
        reject_unc = (~reject_phy) & (unc_scores > tau_unc)
        accept = (~reject_phy) & (~reject_unc)

        metrics = {
            'acceptance_rate': accept.mean(),
            'rejection_phy': reject_phy.mean(),
            'rejection_unc': reject_unc.mean(),
            'accuracy_accepted': accuracy_score(labels[accept], preds[accept]) if accept.sum() > 0 else 0,
            'f1_accepted': f1_score(labels[accept], preds[accept], average='weighted') if accept.sum() > 0 else 0,
            'accuracy_rejected_phy': accuracy_score(labels[reject_phy], preds[reject_phy]) if reject_phy.sum() > 0 else 0,
            'accuracy_rejected_unc': accuracy_score(labels[reject_unc], preds[reject_unc]) if reject_unc.sum() > 0 else 0,
        }

        risk = (reject_phy.sum() + reject_unc.sum()) * lambda_cost
        risk += ((~reject_phy) & (~reject_unc) & (preds != labels)).sum()
        metrics['empirical_risk'] = risk / len(labels)

        return metrics

